package ch.ehi.fgdb4j;

public class Fgdb4jException extends Exception {

	public Fgdb4jException() {
		super();
	}

	public Fgdb4jException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public Fgdb4jException(String arg0) {
		super(arg0);
	}

	public Fgdb4jException(Throwable arg0) {
		super(arg0);
	}

}
